import UIKit

extension UIColor {
    
    @nonobjc class var mainPink: UIColor {
        return UIColor(red: 234.0 / 255.0, green: 69.0 / 255.0, blue: 121.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var backBlack: UIColor {
        return UIColor(red: 28.0 / 255.0, green: 28.0 / 255.0, blue: 28.0 / 255.0, alpha: 1.0)
    }
}
