import UIKit

extension UIColor {
    
    @nonobjc class var mainPink: UIColor {
        return UIColor(red: 234.0 / 255.0, green: 69.0 / 255.0, blue: 121.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var textGray: UIColor {
        return UIColor(white: 173.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var mainWhite: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var mainLine: UIColor {
        return UIColor(white: 53.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var mainBackground: UIColor {
        return UIColor(white: 28.0 / 255.0, alpha: 1.0)
      }

}
